#include <stdio.h>

/**
 * main - prints single digit numbers of base 10 using putchar
 * Return: Always 0 (Success)
 */

int main(void)
{
	/* Print digits 0 to 4 */
	putchar(48);
	putchar(49);
	putchar(50);
	putchar(51);
	putchar(52);

	/* Print digits 5 to 9 */
	putchar(53);
	putchar(54);
	putchar(55);
	putchar(56);
	putchar(57);

	/* Print a new line */
	putchar('\n');

	return (0);
}

